    private void repairbill(){
         String tot=tot_txt.getText();
         SimpleDateFormat sdf=new SimpleDateFormat("yyyy-MM-dd");
         Date date=new Date();
         sdf.format(date);
         int lastbill=0;
         
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con1=DriverManager.getConnection("jdbc:mysql://localhost/supermrket","root","531531");
            String query1="insert into repair_bill(total,date)values(?,?)";
            pdst=con1.prepareStatement(query1,Statement.RETURN_GENERATED_KEYS);
            pdst.setString(1,tot);
            pdst.setString(2,sdf.format(date));
            pdst.executeUpdate();
            
            ResultSet genaeraterskey = pdst.getGeneratedKeys();
            
            if (genaeraterskey.next()){
                lastbill=genaeraterskey.getInt(1);
            }
            
            int rows=cat_table.getRowCount();
            
            for(int i=0;i<cat_table.getRowCount();i++){
                String query2="insert into repair_services(rbill_id,service,amount)values(?,?,?)";
                pdst=con1.prepareStatement(query2);
                
                String service = (String)cat_table.getValueAt(i,1);
                String total = (String)cat_table.getValueAt(i,2);
                
                pdst.setInt(1,lastbill);
                pdst.setString(2,service);
                pdst.setString(3,total);
                
                pdst.executeUpdate();
                
            }
                JOptionPane.showMessageDialog(null,"reapair added!");
                String lastbilll=String.valueOf(lastbill);
                billno.setText(lastbilll);
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(repair.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }
    
    
    
    
    
    
