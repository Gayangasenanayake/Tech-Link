    private void table_update(){
        
        try {
            int c;
            
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                con1=DriverManager.getConnection("jdbc:mysql://localhost/supermrket","root","531531");
                pdst=con1.prepareStatement("select * from supermrket.category");
                ResultSet rs=pdst.executeQuery();
                ResultSetMetaData rsmd=rs.getMetaData();
                c=rsmd.getColumnCount();
                
                DefaultTableModel d1=(DefaultTableModel)cat_table.getModel();
                d1.setRowCount(0);
                
                while(rs.next()){
                    Vector v1=new Vector();
                    for(int i=1;i<=c;i++){ 
                        v1.add(rs.getString("id"));
                        v1.add(rs.getString("category"));
                        v1.add(rs.getString("status"));
                    }
                    d1.addRow(v1);
                }

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(repair.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(repair.class.getName()).log(Level.SEVERE, null, ex);
        }
    } 
    
        
    
    
    
    
    private void getbillno(){
        
        int lastbill=0;
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con1=DriverManager.getConnection("jdbc:mysql://localhost/supermrket","root","531531");
            String query1="select * from repair_bill";
            pdst=con1.prepareStatement(query1,Statement.RETURN_GENERATED_KEYS);
            pdst.executeQuery();
            
            ResultSet genaeraterskey = pdst.getGeneratedKeys();
            
            if (genaeraterskey.next()){
                lastbill=genaeraterskey.getInt(1);
            }
            String lastbilll=String.valueOf(lastbill);
            billno.setText(lastbilll);
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(repair.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }
    
      
